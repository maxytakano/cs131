abstract class BinaryOp extends Operator
{
    //----------------------------------------------------------------
    //
    //----------------------------------------------------------------
    public BinaryOp(String strName)
    {
        super(strName);
    }

    //----------------------------------------------------------------
    // Abstract method for checking operands
    //----------------------------------------------------------------
    public abstract STO checkOperands(STO a, STO b);

}
